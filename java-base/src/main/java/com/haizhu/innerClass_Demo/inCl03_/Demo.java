package com.haizhu.innerClass_Demo.inCl03_;

public class Demo {
	/**
	 * 只用到类的一个实例。 
	 * 类在定义后马上用到。
	 * 类非常小（SUN推荐是在4行代码以下）
	 * 给类命名并不会导致你的代码更容易被理解。
	 * 
	 * 在使用匿名内部类时，要记住以下几个原则：
	 * 匿名内部类不能有构造方法。
	 * 匿名内部类不能定义任何静态成员、方法和类。
	 * 匿名内部类不能有修饰符：public,protected,private,static。
	 * 只能创建匿名内部类的一个实例。
	 * 一个匿名内部类一定是在new的后面，用其隐含实现一个接口或实现一个类。
	 * 因匿名内部类为局部内部类，所以局部内部类的所有限制都对其生效。
	 */
}
